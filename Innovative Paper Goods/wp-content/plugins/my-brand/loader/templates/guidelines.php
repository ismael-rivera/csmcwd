<h3>Themplating with My Brand Login</h3>
<h5>
This is to give you a rough guide as to how you create a template for My Brand Login. <a href="http://forum.gdsweb.ca/viewtopic.php?f=37&t=18" target="_blank">Read Post</a> (on gdsweb.ca).
</h5>
All you're going to be doing is simply writing a Cascading Style Sheet (CSS).
<br/><br/>
The My Brand Login Plugin removes some of the elements automatically, so it lets you start off with basically a blank login page with inputs. All you have to do is add some style.
<br/><br/>
The point of the Template System is to allow you to hook onto CSS calls that the plugin is not currently offering (suggestions welcome) and make a more in depth login page.
<br/><br/>
Everything you create should be uploaded to the file folder under the 'Template' tab of your Options Page.
<br/><br/>
If you wish, you can host the images elsewhere and directly reference them in the CSS Template. 
<br/><br/>
Follow the link at the top of the page for more info.
